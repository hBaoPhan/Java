package gui;

public class PhongBan {
	private String ma;
	private String ten;
	public PhongBan(String ma, String ten) {
		super();
		this.ma = ma;
		this.ten = ten;
	}
	public PhongBan(String ma) {
		super();
		this.ma = ma;
	}
	public String getMa() {
		return ma;
	}
	public void setMa(String ma) {
		this.ma = ma;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	

}
